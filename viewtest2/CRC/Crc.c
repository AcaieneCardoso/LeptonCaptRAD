uint8_t fnucChecksum (uint8_t* pucData, uint8_t ucNumberOfBytes)
{
   uint8_t ucChecksum;
   uint8_t i;
   
   ucChecksum = 0u;
   
   i=0u;
   while (i<ucNumberOfBytes)
   {
      ucChecksum = ucChecksum + pucData[i];
      
      i++;
   }
   
   return (ucChecksum);
}


