/**
 *    @brief Calculate the 16 bits CRC with SEED = 0x8005 according to Lammert Bies website ONLY 
 *           TO PIMA MESSAGE.
 *
 *    @param[in] uint8_t* pucData - Pointer to the first byte (MSB) to calculate CRC (big endian). 
 *    @param[in] uint32_t ulLen - Number of bytes used to calculate CRC (NOT included the CRC 2 bytes.
 *    @param[out]
 *
 *    @return uint16_t The result of CRC.
 *
 *    @note   
 *       To calculate the CRC: 
 *          The CRC, which are 2 bytes, can't be used to calculate CRC. These bytes, which are the result
 *          of this function, must be added by software. LSB must be added first [n] and the MSB must
 *          be added last [n+1].
 *
 *       To check the CRC:
 *           Calculate the CRC of the message without the CRC bytes. Than compare the result of this function
 *           with the CRC received. LSB must be compared with first byte of the CRC received [n] and the MSB must
 *          be compared with the last one [n+1].
 */
/*-----------------------------------------------------------------------------------------------*/ 
uint16_t fnuiCrc16BitsLammertBies(uint8_t* pucBuffer, uint32_t ulLen)
{
   uint32_t ulCnt;
   uint16_t uiCrc;
   
   uiCrc = 0x0000;
   
   for (ulCnt = 0; ulCnt < ulLen; ulCnt++)
   {
      uiCrc = update_crc_16 (uiCrc, *pucBuffer);      
      
      pucBuffer++;
   }
   
   return (uiCrc);
}

   /*******************************************************************\
    *                                                                   *
    *   unsigned short update_crc_16( unsigned short crc, char c );     *
    *                                                                   *
    *   The function update_crc_16 calculates a  new  CRC-16  value     *
    *   based  on  the  previous value of the CRC and the next byte     *
    *   of the data to be checked.                                      *
    *                                                                   *
    \*******************************************************************/

unsigned short update_crc_16( unsigned short crc, char c ) {

    unsigned short tmp, short_c;

    short_c = 0x00ff & (unsigned short) c;

    tmp =  crc       ^ short_c;
    crc = (crc >> 8) ^ auiCrc16LammertBies[ tmp & 0xff ];

    return crc;

}  /* update_crc_16 */


